    /**valida todos os campos
     * 
     * @return 
     */
private boolean validaCampos(){
        
        String entrada = txtEntrada.getText();
        String saida = txtSaida.getText();
        
        boolean valido = true; 

        if(txtEntrada.getText().trim().isEmpty() || txtSaida.getText().trim().isEmpty()){

            JOptionPane.showMessageDialog(rootPane, "Todos os campos devem ser preenchidos. ");

        } else{

            boolean strEntrada = entrada.matches("[0-9]{2}[/][0-9]{2}[/][0-9]{4}");
            boolean strSaida = saida.matches("[0-9]{2}[/][0-9]{2}[/][0-9]{4}");


           if(strEntrada == false && strSaida == false){

               JOptionPane.showMessageDialog(rootPane, "Ambos os campos devem ser no formato (dd/MM/yyyy).");

           } else if(strEntrada == false){

             JOptionPane.showMessageDialog(rootPane, "ATENÇÃO! O primeiro campo deve ser no formato (dd/MM/yyyy).");

           } else if(strSaida == false){

             JOptionPane.showMessageDialog(rootPane, "ATENÇÃO! O segundo campo deve ser no formato (dd/MM/yyyy).");

           } else{

            valido = false;
           }
        }

        return valido;
     }

