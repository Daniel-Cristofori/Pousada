    /**retorna quarto com seus atributos que foram selecionados
     * 
     * @return 
     */
    private Quarto getQuarto() {
        
        List<TipoQuarto> tipoQuartos= (tipoQuartoDAO.listarTipoQuarto());
        
         int numero = Integer.parseInt(txtNumero.getText());
         
         TipoQuarto tipoQuarto = null;
         
         switch ((String) cbxTipoQuarto.getSelectedItem()) {
           
            case "VIP, capacidade: 2, valor: R$ 400,00" -> {
                            
                tipoQuarto = tipoQuartos.get(0);
                        
            }
                       
            case "Normal, capacidade: 2, valor: R$ 250.00" -> {
                          
                tipoQuarto = tipoQuartos.get(1);
                       
            }
                        
            case "Normal, capacidade: 2, valor: R$ 200.00" -> {
                            
                tipoQuarto = tipoQuartos.get(2);
                        
            }
            
            case "Normal, capacidade: 3, valor: R$ 280.00" -> {
                            
                tipoQuarto = tipoQuartos.get(3);
                        
            }
            
            case "Normal, capacidade: 4, valor: R$ 300.00" -> {
                            
                tipoQuarto = tipoQuartos.get(4);
                        
            }
        }
        
         Quarto quarto = new Quarto();
         
         quarto.setNumero(numero);
         quarto.setTipoQuarto(tipoQuarto);

          return quarto;
          
    }
    
    
    
