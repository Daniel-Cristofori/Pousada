    /**botão para passar para proxima etapa da reserva
     * 
     * @param evt 
     */
    private void btnContinuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContinuarActionPerformed
       
       
        if(!validaCampos()) {
            
            DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy"); 
        
            String entrada = txtEntrada.getText();
            String saida = txtSaida.getText();
        
            LocalDate dEntrada = LocalDate.parse(entrada, formato);
            LocalDate dSaida = LocalDate.parse(saida, formato);
            
            Reserva reserva = new Reserva();
            reserva.setData_entrada(dEntrada);
            reserva.setDataSaida(dSaida);
            
            TelaReservaQuarto telaQuartos = new TelaReservaQuarto();
            
            telaQuartos.setReserva(reserva);
            
            telaQuartos.atualizarTabela();
            telaQuartos.setVisible(true);
            
            
            this.dispose();
            
        }
       
    }//GEN-LAST:event_btnContinuarActionPerformed

